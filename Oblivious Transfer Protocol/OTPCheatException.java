
public class OTPCheatException extends OTPException {
    public OTPCheatException() {
        super();
    }
    public OTPCheatException(String msg) {
        super(msg);
    }
    public OTPCheatException(String message, Throwable cause) {
        super(message, cause);
    }
    public OTPCheatException(Throwable cause) {
        super(cause);
    }
}
