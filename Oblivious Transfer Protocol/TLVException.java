
public class TLVException extends RuntimeException {
    public TLVException() {
        super();
    }
    public TLVException(String msg) {
        super(msg);
    }
    public TLVException(String message, Throwable cause) {
        super(message, cause);
    }
    public TLVException(Throwable cause) {
        super(cause);
    }
}
